// Variables 
class Variables
{
	public static void main(String args[])
	{
		int a=10,b=20;
		System.out.println("The Integer values are a="+a+" and b="+b);
		float c=5.5f;
		System.out.println("The float value of c is "+c);
		double d=2.123456789;
		System.out.println("The double value of d is "+d);
		char e='S';
		System.out.println("The character value of e is "+e);
		String f="Jason";
		System.out.println("The String value of f is "+f);
	}
}